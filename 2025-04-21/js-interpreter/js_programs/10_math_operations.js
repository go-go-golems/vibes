// Math operations and functions
const pi = Math.PI;
const e = Math.E;

// Basic math operations
const abs = Math.abs(-5);
const floor = Math.floor(3.7);
const ceil = Math.ceil(3.2);
const round = Math.round(4.5);
const trunc = Math.trunc(6.8);

// Powers and roots
const sqrt = Math.sqrt(16);
const cbrt = Math.cbrt(27);
const pow = Math.pow(2, 3);

// Trigonometric functions
const sin = Math.sin(Math.PI / 2);
const cos = Math.cos(0);
const tan = Math.tan(Math.PI / 4);

// Min, max, random
const min = Math.min(5, 2, 8, 1, 3);
const max = Math.max(5, 2, 8, 1, 3);
const random = Math.random();

// Logarithmic functions
const log = Math.log(10);
const log10 = Math.log10(100);
const log2 = Math.log2(8);

console.log("Constants - PI:", pi, "E:", e);
console.log("Basic operations - abs:", abs, "floor:", floor, "ceil:", ceil, "round:", round, "trunc:", trunc);
console.log("Powers and roots - sqrt:", sqrt, "cbrt:", cbrt, "pow:", pow);
console.log("Trigonometric - sin:", sin, "cos:", cos, "tan:", tan);
console.log("Min/Max - min:", min, "max:", max, "random:", random);
console.log("Logarithmic - log:", log, "log10:", log10, "log2:", log2);
